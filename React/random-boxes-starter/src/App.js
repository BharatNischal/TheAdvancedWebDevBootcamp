import React, { Component } from 'react';
import './App.css';

const NUM_BOXES = 32;

const Box = props => (
  <div style={{ backgroundColor: props.color }}></div>
);

class App extends Component {
  constructor(props) {
    super(props);
    // default boxes
    let boxes = [];
    for (let i = 0; i < NUM_BOXES; i++) {
      boxes.push(props.allColors[i]);
    }
    this.state = {
      boxes
    };

    setInterval(() => {
      const randBoxIdx = Math.floor(
        Math.random() * 
        this.state.boxes.length
      );
      const randColor = Math.floor(
        Math.random() *
        props.allColors.length
      );
      
      const boxes = this.state.boxes.map((box, i) => {
        if (i === randBoxIdx) return props.allColors[randColor];
        return box;
      });
      this.setState({
        boxes
      });
    }, 300);
  }
  
  render() {
    const boxes = this.state.boxes.map((color, idx) => (
      <Box
        key={idx}
        color={color}
      />
    ));
    return (
      <div className="App">
        {boxes}
      </div>
    );
  }
}

